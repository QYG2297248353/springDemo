package com.ms.spring;
/**
 * 定义两个属性
 * 设置getset方法
 * @author Ms
 *
 */
public class Category {
	private int id;
	private String name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
